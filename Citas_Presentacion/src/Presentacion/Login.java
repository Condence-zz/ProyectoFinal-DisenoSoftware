    private static class SampleHighlightPolicy implements DateHighlightPolicy {

        Calendar cal = Calendar.getInstance();
        ControlCitas cc = new ControlCitas();

        @Override
        public HighlightInformation getHighlightInformationOrNull(LocalDate date) {
            LocalDate today = LocalDate.now();
            DateTimeFormatter formatToday = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            today.format(formatToday);

            if (!today.isBefore(date)) {
                return new HighlightInformation(Color.red, null, "");
            }
            if (date.getDayOfWeek() == DayOfWeek.SUNDAY) {
                return new HighlightInformation(Color.red, null, "");
            }
            if (cc.getDuraciones(date) >= 465) {
                return new HighlightInformation(Color.red, null, "");
            }
            if (cc.getDuraciones(date) >= 240) {
                return new HighlightInformation(Color.orange, null, "");
            }
            if (cc.getDuraciones(date) <= 480) {
                return new HighlightInformation(Color.green, null, "");
            }

            return null;
        }
    }

