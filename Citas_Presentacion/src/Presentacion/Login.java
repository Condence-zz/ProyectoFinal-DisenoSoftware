    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        if (jPasswordField1.getText().equals("") || jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar sus datos.", "Advertencia", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String password = new String(jPasswordField1.getPassword());
        Usuario user = log.LoginUsuario(jTextField1.getText(), password);
        if (user != null) {
            JOptionPane.showMessageDialog(this, "Inicio de Sesion Exitoso", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            Animacion.mover_derecha(-290, 0, 2, 2, jPanel5);
            jPanel1.setVisible(false);
            jPanel2.setVisible(true);
            jPanel5.setVisible(true);
            jTextField2.setText(user.getApellido());
            jTextField3.setText(user.getUsername());
            jTextField4.setText(user.getNombre());
            jTextField5.setText(String.valueOf(user.getUsuarioID()));
        } else {
            JOptionPane.showMessageDialog(this, "Inicio Sesion Fallido", "Advertencia", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabel6MouseClicked

